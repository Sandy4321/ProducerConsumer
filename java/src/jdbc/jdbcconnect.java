class Godown { 
    public static final int max_size = 100; //最大库存量 
    public int curnum;     //当前库存量 
    Godown() { 
    } 
    Godown(int curnum) {
    	Connection conn = null;
    	try {
			Class.forName("org.gjt.mm.mysql.Driver");
		} catch (ClassNotFoundException e) {
			System.out.println("无法加载驱动");
			e.printStackTrace();
		}
		try {
			conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/procons?characterEncoding=utf8&useSSL=false","root","123456");
		} catch (SQLException e) {
			System.out.println("无法连接数据库");
			e.printStackTrace();
		}
        try {
    		Statement stmt = conn.createStatement();
    		stmt.executeUpdate("insert into storage (id,num,sum) values (0,0," + curnum + ")");
    		stmt.close();
    		conn.close();
    	} catch (SQLException e) {
   			System.out.println("无法插入初始化数据");
   			e.printStackTrace();
   		}
        System.out.println(0 + ".目前库存为:" + curnum);
        this.curnum = curnum;
    }
    
    public synchronized void produce(int id, int neednum) { 
            //测试是否需要生产 
        while (neednum + curnum > max_size) { 
            System.out.println("要生产的产品数量" + neednum + "超过剩余库存量" + (max_size - curnum) + "，暂时不能执行生产任务!"); 
            try {  
                wait(); 
            } catch (InterruptedException e) { 
                e.printStackTrace(); 
            } 
        } 
      
        //满足生产条件，则进行生产，这里简单的更改当前库存量 
        curnum += neednum; 
        Connection conn = null;
      	try {
    		Class.forName("org.gjt.mm.mysql.Driver");
    	} catch (ClassNotFoundException e) {
   		System.out.println("无法加载驱动");
    		e.printStackTrace();
    	}
    	try {
    		conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/procons?characterEncoding=utf8&useSSL=false","root","123456");
   		} catch (SQLException e) {
    		System.out.println("无法连接数据库");
    		e.printStackTrace();
    	}
        try {
       		Statement stmt = conn.createStatement();
       		stmt.executeUpdate("insert into storage(id,num,sum) values("+ id +","+ neednum +","+ curnum +")");
       		stmt.close();
       		conn.close();
       	} catch (SQLException e) {
       		System.out.println("无法插入生产数据");
       	}
        System.out.println(id + ".已经生产了" + neednum + "个产品，现仓储量为" + curnum);
        //唤醒在此对象监视器上等待的所有线程 
        notifyAll(); 
    } 
    public synchronized void consume(int id, int neednum) { 
            //测试是否可消费 
        while (curnum < neednum) { 
            try { 
                           //当前的生产线程等待 
                wait(); 
            } catch (InterruptedException e) { 
                e.printStackTrace(); 
            } 
        } 

            //满足消费条件，则进行消费，这里简单的更改当前库存量 
        curnum -= neednum; 
        Connection conn = null;
       	try {
    		Class.forName("org.gjt.mm.mysql.Driver");
    	} catch (ClassNotFoundException e) {
    		System.out.println("无法加载驱动");
    		e.printStackTrace();
    	}
    	try {
    		conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/procons?characterEncoding=utf8&useSSL=false","root","123456");
    	} catch (SQLException e) {
    		System.out.println("无法连接数据库");
    		e.printStackTrace();
    	}
        try {
        	Statement stmt = conn.createStatement();
        	stmt.executeUpdate("insert into storage (id,num,sum) values (" + id + "," + (-neednum) + "," + curnum + ")");
        	stmt.close();
        	conn.close();
        } catch (SQLException e) {
       		System.out.println("无法插入消费数据");
       	}
        System.out.println(id + ".已经消费了" + neednum + "个产品，现仓储量为" + curnum); 
            //唤醒在此对象监视器上等待的所有线程 
        notifyAll(); 
    } 
} 

class Producer extends Thread {
	private int id;
    private int neednum;                //生产产品的数量 
    private Godown godown;            //仓库 

    Producer(int id, int neednum, Godown godown) {
    	this.id = id;
        this.neednum = neednum; 
        this.godown = godown; 
    } 

    public void run() { 
            //生产指定数量的产品 
            godown.produce(id,neednum); 
    } 
} 

class Consumer extends Thread { 
	private int id;
    private int neednum;                //生产产品的数量 
    private Godown godown;            //仓库 

    Consumer(int id, int neednum, Godown godown) { 
    	this.id = id;
        this.neednum = neednum; 
        this.godown = godown; 
    } 

    public void run() { 
            //消费指定数量的产品 
            godown.consume(id,neednum); 
    } 
}
